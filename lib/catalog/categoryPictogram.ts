/** Titre sans accents, minuscules. */
export function normalizeCategoryTitle(s: string): string {
  return s
    .normalize("NFD")
    .replace(/\p{M}/gu, "")
    .toLowerCase()
    .trim();
}

/** Mots alphanumériques (évite les faux positifs type « vin » dans « vinaigre »). */
function tokens(n: string): string[] {
  return n.split(/[^a-z0-9]+/).filter(Boolean);
}

