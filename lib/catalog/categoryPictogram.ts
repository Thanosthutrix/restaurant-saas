const FALLBACK: LucideIcon = FolderTree;

function iconFromTokens(n: string): LucideIcon | null {
  const toks = tokens(n);
  const set = new Set(toks);
  for (const [key, Icon] of TOKEN_ICONS) {
    if (set.has(key)) return Icon;
  }
  return null;
}

/**
 * Icône Lucide pour un libellé de rubrique (nom affiché ou segment de chemin).
 */
export function resolveLucideIconForCategoryTitle(title: string): LucideIcon {
  const raw = title.trim();
  if (!raw) return FALLBACK;
  const n = normalizeCategoryTitle(raw);

  for (const { re, Icon } of PHRASE_RULES) {
    if (re.test(n)) return Icon;
  }

  const fromTok = iconFromTokens(n);
  if (fromTok) return fromTok;

  for (const { re, Icon } of REGEX_TAIL) {
    if (re.test(n)) return Icon;
  }

  return FALLBACK;
}
